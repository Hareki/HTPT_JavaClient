package model;

import java.time.LocalDateTime;

public class FileInfo {
    private int id;
    private String name;
    private long size;
    private LocalDateTime uploadedDate;

    public FileInfo() {
        super();
    }
    
    public FileInfo(int id, String name, long size, LocalDateTime uploadedDate) {
        this.id = id;
        this.name = name;
        this.size = size;
        this.uploadedDate = uploadedDate;
    }

    public LocalDateTime getUploadedDate() {
        return uploadedDate;
    }

    public void setUploadedDate(LocalDateTime uploadedDate) {
        this.uploadedDate = uploadedDate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getSize() {
        return size;
    }

    public void setSize(long size) {
        this.size = size;
    }
}
